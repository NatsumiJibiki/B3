package gamesuper05;

public class Character {
	protected int x = 0;
	protected int y = 0;
	protected int xSize = 24;
	protected int ySize = 24;

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getXSize() {
		return xSize;
	}

	public int getYSize() {
		return ySize;
	}

}
